#   define vCout            std::wcout
#   define vCerr            std::wcerr
#   define vClog            std::wclog
#   define vEndl            std::endl
// }}}
#else  // default definition : _V_USE_WCHAR_ == 0
// {{{
#   define vString          std::string

#   define vLocale_o(x)
#   define vLocale_e(x)
#   define vLocale_l(x)
#   define vCin             std::cin
