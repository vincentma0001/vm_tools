// ------------------------------------------------------------------------------------------------ //
// {{{
#   define vString          std::wstring

// Note : 如果在wstring中包含中文，需要在输出前添加语言设置
#   define vLocale_o(x)     std::wcout.imbue(std::locale(x))
#   define vLocale_e(x)     std::wcerr.imbue(std::locale(x))
